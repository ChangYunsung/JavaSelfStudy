package ch1;

public class ch1_SpecialCharEx {
/*
 * 2021.01.20
 * 책 = 자바의 정석
 * 기본형 - 특수문자 다루기
 */
	public static void main(String[] args) {
		System.out.println('\'');
		// '(작은따옴표) 출력
		System.out.println('\n');
		// 개행
		System.out.println("\"Hello\"");
		// "(큰따옴표) 출력
		System.out.println("c:\\");
		// /역슬래쉬 출력
	}

}
